public class Nota {
    private float valor;
    public Nota(){
        this.valor = 0;
    }
    public Nota(float valor){
        this.valor = valor;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }
}